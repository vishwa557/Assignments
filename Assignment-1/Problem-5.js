

//4. how to iterate over an array? explain with an example


//To iterate over an array in JavaScript, we use for loops, 

//example
const numbers = [1, 2, 3, 4, 5];

for (let i = 0; i < numbers.length; i++) {
  console.log(numbers[i]);
}


/**
 In this example, we use a for loop to iterate over the numbers array. The loop initializes i to 0,
  and it continues until i is less than the length of the array. We access each element using the index i.
 */